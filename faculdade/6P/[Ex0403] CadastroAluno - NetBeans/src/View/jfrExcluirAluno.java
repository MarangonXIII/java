    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            // Captura o ID digitado pelo usuário
            int id = Integer.parseInt(jIDAluno.getText());

            // Usando o Controller para excluir o aluno
            ControllerAluno controller = new ControllerAluno();
            controller.excluirAluno(id);

            JOptionPane.showMessageDialog(this, "Aluno excluído com sucesso!");

            // Limpar o campo de ID após a exclusão
            jIDAluno.setText("");
            
            // Fechar a janela atual e voltar para a tela principal
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um ID válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o cadastro: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

